#include <iostream>
using namespace std;
int main() {
	cout << "x+y+z=6" << endl;
	cout << "2x−y+3z=14" << endl;
	cout << "−x+4y−z=2" << endl;
	double m[3][4] = {
		{3, -1, 2, 5},
		{2, 4, -1, 6},
		{1, -3, 1, 4}
	};
	double det = m[0][0] * (m[1][1] * m[2][2] - m[1][2] * m[2][1]) - m[0][1] * (m[1][0] * m[2][2] - m[1][2] * m[2][0]) + m[0][2] * (m[1][0] * m[2][1] - m[1][1] * m[2][0]);
	double det1 = m[0][3] * (m[1][1] * m[2][2] - m[1][2] * m[2][1]) - m[0][1] * (m[1][3] * m[2][2] - m[1][2] * m[2][3]) + m[0][2] * (m[1][3] * m[2][1] - m[1][1] * m[2][3]);
	double det2 = m[0][0] * (m[1][3] * m[2][2] - m[1][2] * m[2][3]) - m[0][3] * (m[1][0] * m[2][2] - m[1][2] * m[2][0]) + m[0][2] * (m[1][0] * m[2][3] - m[1][3] * m[2][0]);
	double det3 = m[0][0] * (m[1][1] * m[2][3] - m[1][3] * m[2][1]) - m[0][1] * (m[1][0] * m[2][3] - m[1][3] * m[2][0]) + m[0][3] * (m[1][0] * m[2][1] - m[1][1] * m[2][0]);
	double arr[3] = { det1 / det, det2 / det, det3 / det };
	for (int i = 0; i < 3; i++) {
		cout << arr[i] << endl;
	}
}